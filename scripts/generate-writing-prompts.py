import re
match = re.search(r'export const samplePrompts: Prompt\[\] = \[', content)
if not match:
    print('❌ 无法找到 samplePrompts 数组')
    exit(1)

prompts_start = match.end()

# 找到匹配的结束括号
bracket_count = 1
prompts_end = prompts_start
for i in range(prompts_start, len(content)):
    if content[i] == '[':
        bracket_count += 1
    elif content[i] == ']':
        bracket_count -= 1
        if bracket_count == 0:
            prompts_end = i
            break

# 生成新的 prompts TypeScript 代码
def prompt_to_ts(prompt):
    return f'''  {{
    "id": "{prompt['id']}",
    "title": "{prompt['title']}",
    "description": "{prompt['description']}",
    "category": "{prompt['category']}",
    "content": {json.dumps(prompt['content'], ensure_ascii=False)},
    "tags": {json.dumps(prompt['tags'], ensure_ascii=False)},
    "author": "{prompt['author']}",
    "createdAt": "{prompt['createdAt']}",
    "likes": {prompt['likes']},
    "uses": {prompt['uses']},
    "examples": {json.dumps(prompt['examples'], ensure_ascii=False, indent=6)}
  }}'''

